function getLanguage() {
  return localStorage.getItem('abaceria-lang') || 'es';
}

function t(key, params) {
  const lang = getLanguage();
  let text = (translations[lang] && translations[lang][key]) || (translations.es && translations.es[key]) || key;
  if (params) {
    Object.keys(params).forEach(k => {
      text = text.replace(new RegExp('\\{' + k + '\\}', 'g'), params[k]);
    });
  }
  return text;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.getAttribute('data-i18n-html'));
  });
}

function setLanguage(lang) {
  localStorage.setItem('abaceria-lang', lang);
  applyTranslations();
  // Update language selector
  const sel = document.getElementById('lang-selector');
  if (sel) sel.value = lang;
  // Re-navigate to refresh dynamic content
  if (typeof navigateTo === 'function' && currentPage) {
    navigateTo(currentPage);
  }
}
